// Get random questions for a test (6-8 questions)
export function getRandomQuestions(subject, count = 7) {
  const subjectQuestions = questions[subject];
  const shuffled = [...subjectQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Check answer with case insensitivity
export function checkAnswer(question, userAnswer) {
  return userAnswer.trim().toLowerCase() === question.answer.toLowerCase();
}
